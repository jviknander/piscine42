int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*(str + i) != '\0')
	{
		i++;
	}
	return (i);
}
int	main()
{
	char	str[] = "ciao";
	ft_strlen(str);
	printf("%d", 5);
}
