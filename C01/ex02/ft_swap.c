#include <stdio.h>

void	ft_swap(int	*a, int	*b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}
int	main()
{
	int	a;
	int	b;

	a = 6;
	b = 8;
	ft_swap(&a, &b);
	printf("%d", a);
	printf("%d", b);
}
